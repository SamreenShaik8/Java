public class Oper {
    public static void main(String[] args) {
        int a=10;
        // pre increment
        System.out.println(++a);
        // post increment
        System.out.println(a++);
        // pre decrement
        System.out.println(--a);
        // post decrement
        System.out.println(a--);
        System.out.println(a);
    }
}
